import json
import random
from typing import Dict, Iterable

from aiohttp.client import ClientSession, ClientTimeout


def create_session(agents: Iterable[str], cookies: Dict, total_timeout: int):
    headers = {
            'User-Agent': random.choice(agents)
    }

    timeout = ClientTimeout(total=total_timeout)
    
    return ClientSession(cookies=cookies, headers=headers, timeout=timeout)


def get_cookies(file: str) -> Dict:
    with open(file) as f:
        data = f.read()

        return json.loads(data)


